#!/usr/bin/env node
/**
 * fetch-os-contributions.mjs
 *
 * Fetches all PRs authored by GITHUB_USERNAME from GitHub's Search API,
 * filters out PRs against the user's own repos, and writes the result
 * to data/openSourceContributions.ts as a typed array.
 *
 * Usage:
 *   GITHUB_TOKEN=ghp_xxx node scripts/fetch-os-contributions.mjs
 *
 * The token only needs the default public scopes (no special permissions).
 * Without a token the API still works but is rate-limited to 10 req/min.
 */

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));

// ─── Config ──────────────────────────────────────────────────────────────────
const GITHUB_USERNAME = 'Karman-singh15';
const GITHUB_TOKEN = process.env.GITHUB_TOKEN ?? '';
const OUTPUT_FILE = path.join(__dirname, '..', 'data', 'openSourceContributions.ts');
// ─────────────────────────────────────────────────────────────────────────────

const headers = {
    Accept: 'application/vnd.github+json',
    'X-GitHub-Api-Version': '2022-11-28',
    ...(GITHUB_TOKEN ? { Authorization: `Bearer ${GITHUB_TOKEN}` } : {}),
};

/**
 * Fetch a single page of search results.
 */
async function fetchPage(page) {
    const q = encodeURIComponent(
        `type:pr author:${GITHUB_USERNAME} -user:${GITHUB_USERNAME} -user:realhardik18`
    );
    const url = `https://api.github.com/search/issues?q=${q}&per_page=100&page=${page}&sort=created&order=desc`;

    const res = await fetch(url, { headers });
    if (!res.ok) {
        const body = await res.text();
        throw new Error(`GitHub API error ${res.status}: ${body}`);
    }
    return res.json();
}

/**
 * Fetch ALL pages of matching PRs.
 */
async function fetchAllPRs() {
    const all = [];
    let page = 1;

    while (true) {
        console.log(`  Fetching page ${page}...`);
        const data = await fetchPage(page);

        all.push(...data.items);

        // GitHub Search API caps at 1 000 results
        if (all.length >= data.total_count || data.items.length < 100) break;
        page++;
    }

    console.log(`  → Found ${all.length} PRs total (after API filter: -user:${GITHUB_USERNAME})`);
    return all;
}

/**
 * Convert a raw GH search item to the shape we want.
 */
function toContribution(item) {
    // repository_url looks like https://api.github.com/repos/owner/name
    const repoPath = item.repository_url.replace('https://api.github.com/repos/', '');
    return {
        repo: repoPath,                   // e.g. "pyrefly/pyrefly"
        title: item.title,                 // PR title
        url: item.html_url,              // https://github.com/...
        state: item.pull_request?.merged_at ? 'merged' : item.state,
        createdAt: item.created_at.slice(0, 10), // "YYYY-MM-DD"
    };
}

/**
 * Write the contributions array to a .ts file.
 */
function writeOutput(contributions) {
    fs.mkdirSync(path.dirname(OUTPUT_FILE), { recursive: true });

    const lines = [
        '// AUTO-GENERATED by scripts/fetch-os-contributions.mjs',
        `// Last updated: ${new Date().toISOString()}`,
        '// Do not edit manually — run: npm run fetch:os',
        '',
        'export interface OSContribution {',
        '    repo:      string;',
        '    title:     string;',
        '    url:       string;',
        '    state:     "open" | "closed" | "merged";',
        '    createdAt: string;',
        '}',
        '',
        'export const openSourceContributions: OSContribution[] = [',
        ...contributions.map(c =>
            `    { repo: ${JSON.stringify(c.repo)}, title: ${JSON.stringify(c.title)}, url: ${JSON.stringify(c.url)}, state: ${JSON.stringify(c.state)}, createdAt: ${JSON.stringify(c.createdAt)} },`
        ),
        '];',
        '',
    ];

    fs.writeFileSync(OUTPUT_FILE, lines.join('\n'), 'utf8');
    console.log(`\n✅ Written ${contributions.length} contributions to:\n   ${OUTPUT_FILE}`);
}

// ─── Main ────────────────────────────────────────────────────────────────────
(async () => {
    console.log(`\n🔍 Fetching open-source PRs for @${GITHUB_USERNAME}...\n`);

    if (!GITHUB_TOKEN) {
        console.warn('⚠️  No GITHUB_TOKEN set — you may hit the 10 req/min unauthenticated limit.');
        console.warn('   Set it with: export GITHUB_TOKEN=ghp_...\n');
    }

    try {
        const rawPRs = await fetchAllPRs();
        const contributions = rawPRs.map(toContribution);
        writeOutput(contributions);
    } catch (err) {
        console.error('\n❌ Error:', err.message);
        process.exit(1);
    }
})();
